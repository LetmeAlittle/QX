    /**
     * 对图片进行base64编码
     *
     * @param imgPath
     * @param size    图片大小
     * @return
     */
    public static String base64Encode(String imgPath, int size) {
        //首先对图片进行压缩处理然后再进行 Base64编码
        String baseResult;
        BitmapFactory.Options options = new BitmapFactory.Options();
        options.inSampleSize = getImgSample(imgPath, size, (int) (size * 1.6));//指定宽高，获取图片采样率 动态获取 达到更优化
//        options.inSampleSize =7;//指定宽高，获取图片采样率 动态获取 达到更优化
        Bitmap bitmap = BitmapFactory.decodeFile(imgPath, options);
        //压缩图片到指定大小
        ByteArrayOutputStream baos = new ByteArrayOutputStream();
        bitmap.compress(Bitmap.CompressFormat.PNG, 100, baos);
        // 质量压缩方法，这里100表示不压缩，把压缩后的数据存放到baos中
        int quality = 90;
        while (baos.toByteArray().length / 1024 > size) {
            // 循环判断如果压缩后图片是否大于300kb,大于继续压缩
            baos.reset();// 重置baos即清空baos
            bitmap.compress(Bitmap.CompressFormat.PNG, quality, baos);
            // 这里压缩options%，把压缩后的数据存放到baos中
            quality -= 10;// 每次都减少10
            if (quality <= 0) {
                break;
            }
        }
        byte[] bytes = baos.toByteArray();
        //编码
        baseResult = Base64.encodeToString(bytes, Base64.DEFAULT);
        return baseResult;
    }

